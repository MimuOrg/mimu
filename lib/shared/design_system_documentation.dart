/// Документация системы дизайна Mimu с богатым черным дизайном
/// 
/// Этот файл содержит описание всех изменений в дизайн-системе приложения Mimu.
/// Все фиолетовые цвета были заменены на богатую черную палитру для создания
/// премиум и современного внешнего вида.
/// 
/// ОСНОВНЫЕ ИЗМЕНЕНИЯ:
/// 
/// 1. ЦВЕТОВАЯ ПАЛИТРА
///    - Rich Black (#000000) - Чистый черный для фонов
///    - Deep Black (#050505) - Глубокий черный для поверхностей
///    - Charcoal Black (#0A0A0A) - Угольно-черный для акцентов
///    - Dark Charcoal (#0F0F0F) - Темный уголь для карточек
///    - Medium Black (#1A1A1A) - Средний черный для интерактивных элементов
///    - Light Black (#252525) - Светлый черный для границ
/// 
/// 2. КОМПОНЕНТЫ
///    - RichBlackCard - Улучшенные карточки с богатым черным дизайном
///    - RichBlackButton - Кнопки с анимациями и богатым черным дизайном
///    - RichBlackLoader - Индикаторы загрузки
///    - RichBlackChip - Чипы для фильтров и тегов
///    - RichBlackListTile - Элементы списка
///    - RichBlackSearchBar - Улучшенный поиск
/// 
/// 3. АНИМАЦИИ
///    - fadeScaleIn - Плавное появление с масштабированием
///    - slideUpFade - Скольжение снизу с затемнением
///    - pulseGlow - Пульсирующее свечение
///    - rotateScale - Вращение с масштабированием
///    - wave - Эффект волны
///    - bounceUp - Появление снизу с отскоком
/// 
/// 4. ПЕРЕХОДЫ
///    - RichBlackPageRoute - Плавные переходы между экранами
///    - fadeTransition - Переход с затемнением
///    - scaleTransition - Переход с масштабированием
///    - rotationTransition - Переход с поворотом
/// 
/// 5. МИКРОИНТЕРАКЦИИ
///    - hapticFeedback - Тактильная обратная связь
///    - successAnimation - Анимация успешного действия
///    - errorAnimation - Анимация ошибки
/// 
/// 6. ПОИСК И ФИЛЬТРЫ
///    - EnhancedSearchBar - Улучшенный поиск с фильтрами
///    - EnhancedSortMenu - Меню сортировки
///    - EnhancedFilters - Расширенные фильтры
///    - QuickSearchActions - Быстрые действия поиска
/// 
/// ОБНОВЛЕННЫЕ ФАЙЛЫ:
/// 
/// - lib/app/theme.dart
///   - Заменены все фиолетовые цвета на богатые черные оттенки
///   - Добавлена палитра RichBlackPalette
///   - Обновлены все темы приложения
/// 
/// - lib/features/settings_hub.dart
///   - Обновлена цветовая палитра акцентных цветов
///   - Добавлены новые темы с черными оттенками
///   - Обновлены все UI элементы
/// 
/// - lib/shared/glass_widgets.dart
///   - Улучшены эффекты стекла с черными оттенками
///   - Добавлены тени и свечения
/// 
/// - lib/shared/animated_widgets.dart
///   - Обновлены анимации с черными акцентами
///   - Добавлены новые эффекты
/// 
/// - lib/shared/cupertino_dialogs.dart
///   - Обновлены диалоги с черной палитрой
///   - Улучшены визуальные эффекты
/// 
/// - lib/features/shell_ui.dart
///   - Обновлена навигация с новым дизайном
///   - Улучшены кнопки и индикаторы
/// 
/// - lib/features/chat_screen.dart
///   - Обновлены пузыри сообщений
///   - Улучшены индикаторы и UI элементы
/// 
/// - lib/features/create_entities.dart
///   - Обновлены экраны создания групп/каналов
///   - Добавлен новый дизайн
/// 
/// НОВЫЕ ФАЙЛЫ:
/// 
/// - lib/shared/enhanced_ui_components.dart
///   - Новые UI компоненты с богатым черным дизайном
///   - Карточки, кнопки, индикаторы загрузки
/// 
/// - lib/shared/rich_black_animations.dart
///   - Расширенные анимации
///   - Переходы между экранами
///   - Микроинтеракции
/// 
/// - lib/shared/enhanced_search_filters.dart
///   - Улучшенный поиск
///   - Фильтры и сортировка
///   - Быстрые действия
/// 
/// ПРИМЕРЫ ИСПОЛЬЗОВАНИЯ:
/// 
/// ```dart
/// // Использование RichBlackCard
/// RichBlackCard(
///   padding: EdgeInsets.all(16),
///   useGlow: true,
///   child: Text('Контент карточки'),
/// )
/// 
/// // Использование RichBlackButton
/// RichBlackButton(
///   icon: CupertinoIcons.add,
///   onPressed: () {},
///   child: Text('Кнопка'),
/// )
/// 
/// // Использование анимаций
/// RichBlackAnimations.fadeScaleIn(
///   child: Widget(),
///   delay: Duration(milliseconds: 100),
/// )
/// 
/// // Использование поиска
/// EnhancedSearchBar(
///   hintText: 'Поиск...',
///   filterOptions: ['Все', 'Люди', 'Группы'],
///   onChanged: (query) {},
/// )
/// ```
/// 
/// ПРИМЕЧАНИЯ:
/// 
/// - Все изменения глобальные и затрагивают все аспекты приложения
